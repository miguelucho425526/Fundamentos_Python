# Variables para el número de manzanas de cada persona
john = 3
mary = 5
adam = 6

# Imprimir las variables en una línea, separadas por comas
print(john, mary, adam)

# Sumar el total de manzanas
total_apples = john + mary + adam

# Imprimir el total de manzanas
print(total_apples)

# Experimentando con nuevas variables y operaciones
juan = 2
ana = 4

# Suma y resta
resultado = juan + ana - john
print("Resultado de juan + ana - john:", resultado)

# Multiplicación
multiplicacion = mary * 2
print("Mary tiene el doble de manzanas:", multiplicacion)

# División
division = adam / juan
print("Adán tiene dividido entre Juan:", division)

# División entera
division_entera = adam // juan
print("División entera de Adán entre Juan:", division_entera)

# Concatenar texto y número usando coma
print("Número total de manzanas:", total_apples)

# Concatenar texto y número usando f-string
print(f"Juan, María y Adán tienen en total {total_apples} manzanas.")